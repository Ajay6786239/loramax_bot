var search = document.querySelector("#search");
var productcontainer = document.querySelector(".cat");
var productlist = [];

// Assuming your class names are cat1, cat2, ..., cat35
for (var i = 1; i <= 35; i++) {
    productlist[i] = document.querySelector(`.cat${i}`);
}

// Now you can use productlist[i] to access each product
console.log(productlist);

search.addEventListener("keyup", function(event) {
    var enteredvalue = event.target.value.toUpperCase();

    for (var count = 1; count < productlist.length; count++) {
        var product = productlist[count];
        
        // Ensure product exists
        if (product) {
            var productname = product.querySelector("a") ? product.querySelector("a").textContent : '';

            if (productname.toUpperCase().indexOf(enteredvalue) < 0) {
                product.style.display = "none";
            } else {
                product.style.display = "block";
            }
        }
    }
});







